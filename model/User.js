const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const userSchema = new Schema({
    usermail:{
        type: String,
        required: true
    },
    phone:{
        type: Number,
        required: true
    },
    roles:{
        User:{
            type: Number,
            default: 2001
        },
        Editor: Number,
        Admin: Number
    },
    password:{
        type: String,
        required: true
    },
    firstname:{
        type: String,
        required: true
    },
    lastname:{
        type: String,
        required: true
    },
    refreshToken: String
});
module.exports = mongoose.model('User', userSchema);